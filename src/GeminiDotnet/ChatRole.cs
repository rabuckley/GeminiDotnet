namespace GeminiDotnet;

/// <summary>
/// Defines the role values used in Gemini API content messages.
/// </summary>
public static class ChatRoles
{
    /// <summary>
    /// The role representing user-provided content.
    /// </summary>
    public const string User = "user";

    /// <summary>
    /// The role representing model-generated content.
    /// </summary>
    public const string Model = "model";
}
